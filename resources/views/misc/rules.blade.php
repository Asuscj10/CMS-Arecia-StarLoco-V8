<div>
    <h3>➲ <b>La advertencia</b></h3>
</div>
<p>
    Para ciertas infracciones, se le da una advertencia al jugador para informarle de las reglas y advertirle que corre el riesgo de una sanción. Esta advertencia es enviada directamente en el juego por los moderadores o por correo electrónico de parte de Nyeera. No caduca y se considera leída y comprendida por el jugador.
</p>
<div>
    <h3>➲ <b>El mute</b></h3>
</div>
<p>
    La privación de palabra es una de las sanciones aplicadas por los moderadores contra los jugadores que no respetan las reglas de DOFUS. Puede ir acompañada de una solicitud de suspensión de cuenta al Soporte.
</p>
<div>
    <h3>➲ <b>La expulsión (kick)</b></h3>
</div>
<p>
    La expulsión del juego por un tiempo determinado es aplicada por los moderadores para los casos más graves. Generalmente va acompañada de una solicitud de suspensión de cuenta al Soporte.
</p>
<div>
    <h3>➲ <b>La suspensión temporal</b></h3>
</div>
<p>
    La cuenta es inaccesible durante varios días (3, 7, 15 o 30 días). Esta suspensión provisional, aplicada por el Soporte, es un paso que a veces precede al baneo definitivo de la cuenta de DOFUS. Su duración depende de la gravedad de la falta y de la reincidencia.
</p>
<div>
    <h3>➲ <b>La suspensión definitiva</b></h3>
</div>
<p>
    La cuenta se cierra definitivamente, el jugador ya no puede acceder a ella. Esta sanción se aplica generalmente en casos de jugadores reincidentes o de incumplimiento imperdonable de las reglas de DOFUS.

    La severidad de una sanción depende del nivel de gravedad de la infracción cometida. En general, varias advertencias conducen a un baneo y varios baneos conducen a una exclusión definitiva. Sin embargo, algunas infracciones dan lugar directamente a un baneo definitivo. Del mismo modo, la reincidencia en una infracción también conlleva una exclusión firme.
</p>
<div class="alert_message blue">Lista no exhaustiva de las reglas de buena conducta que se deben respetar imperativamente</div>
<div>
    <h3>➲ <b>Cuentas, personajes, nombres y apodos</b></h3>
</div>
<p>
    Los apodos de cuentas, personajes y nombres de gremios deben respetar las siguientes reglas:
</p>
<ul>
    <li>Corresponder a una palabra pronunciable (por ejemplo, la palabra ssdfzxjf no respeta esta regla)</li>
    <li>No hacer referencia a una orientación política, una etnia, una comunidad o una religión</li>
    <li>No ser vulgares ni insultantes</li>
    <li>No tener connotaciones sexuales o pornográficas</li>
    <li>No contener tu nombre y/o apellido</li>
    <li>No imitar una marca registrada</li>
    <li>No hacer referencia a un estupefaciente o a cualquier otra entidad prohibida por la legislación</li>
    <li>No sugerir prácticas prohibidas por las reglas del juego</li>
    <li>No imitar los nombres de personajes del trasfondo de DOFUS (personajes no jugadores, héroes de la historia...)</li>
    <li>No imitar el nombre de un moderador o de un miembro de la Sociedad</li>
    <li>No ser escrito o deletreado alternativamente con el fin de eludir las reglas impuestas anteriormente.</li>
</ul>
<p>
    Está prohibido prestar la cuenta, debido a los diversos abusos posibles (uso de la cuenta prestada para cometer infracciones, robos...). En caso de incumplimiento de esta regla, ambos protagonistas (el que presta y el que recibe) incurrirán en sanciones.

    La venta, compra, intercambio y donación de cuentas están prohibidos. Las cuentas son propiedad de Nyeera y, como tales, no se puede obtener ningún beneficio pecuniario de ellas, ya sea en moneda real o virtual (kamas). Además, una cuenta está asociada a datos personales que es imprudente divulgar. El propietario original de la cuenta siempre sigue siendo responsable de las acciones de su personaje. Puede ser procesado penalmente en caso de acciones graves realizadas con su cuenta.
</p>
<div>
    <h3>➲ <b>Canales de discusión</b></h3>
</div>
<ul>
    <li>El "flood", que consiste en repetir el mismo mensaje varias veces en un corto período de tiempo, no está permitido.</li>
    <li>El comercio en Chat General (en el canal común de color negro) está prohibido fuera de los mapas de mercaderes*. Se considera "flood".</li>
    <li>La reclutación en Chat General también está prohibida.</li>
    <li>Cada canal tiene su función. Esta se indica al pasar el ratón por la casilla de verificación para activar/desactivar dicho canal. Está prohibido usar un canal de manera inapropiada.</li>
    <li>Está prohibido abusar de caracteres especiales, resaltar el mensaje escribiéndolo en mayúsculas, repetir un carácter para llamar la atención, o usar el símbolo @ o cualquier otro carácter que permita mostrar el mensaje automático de advertencia contra sitios peligrosos "Atención".</li>
    <li>Está prohibido hacer publicidad de un sitio de terceros, incluidos los relacionados con DOFUS.</li>
</ul>
<div>
    <h3>➲ <b>Animaciones</b></h3>
</div>
<ul>
    <li>La organización, sin autorización, de loterías que apelen a la generosidad de los jugadores para la constitución de los premios está prohibida, siendo el riesgo de robo o estafa demasiado elevado.</li>
    <li>Está prohibido organizar reuniones cuyo único propósito sea crear un movimiento masivo, esto para mantener la estabilidad de los servidores de DOFUS y la calidad de juego que de ello se deriva.</li>
</ul>
<div>
    <h3>➲ <b>Respeto a los demás jugadores</b></h3>
</div>
<ul>
    <li>Se exigen expresiones correctas, esto con el fin de mantener un ambiente amigable en los servidores. La vulgaridad, los insultos, los discursos racistas, xenófobos, homófobos, sexuales y, en general, todas las expresiones inapropiadas están prohibidas.</li>
    <li>El Servidor se le asocia un idioma. Se recomienda utilizar únicamente este en los canales públicos. Sin embargo, eres libre de hablar en otro idioma.</li>
    <li>Las estafas están prohibidas. Nunca es agradable darse cuenta de que uno ha sido engañado de alguna manera. En este sentido, todas las estafas detectadas directamente en el juego por los moderadores serán sancionadas.</li>
    <li>La suplantación de identidad está prohibida, ya sea la de un jugador, un gremio, un moderador o un miembro de Nyeera, e independientemente de la intención (ya sea buena o mala).</li>
    <li>Divulgar la información personal de otro jugador está prohibido.</li>
    <li>En general, está prohibido obstaculizar el buen desarrollo del juego de otros jugadores.</li>
</ul>
